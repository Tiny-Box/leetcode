void QuickSort(vector<int> e, int first, int end)  
{  
    int i=first,j=end;  
    int temp=e[first];//记录第一个数据  
      
    while(i<j)  
  {  
        while(i<j && e[j]>=temp)  //与first数据比较，右边下标逐渐左移  
            j--;  
  
        e[i]=e[j];        
  
        while(i<j && e[i]<=temp)  //与first数据比较，左边下标逐渐右移  
            i++;          
  
        e[j]=e[i];  
  }  
    e[i]=temp;                      //将first数据放置于i=j处  
  
    if(first<i-1)  
    QuickSort(e,first,i-1);  
    if(end>i+1)        
    QuickSort(e,i+1,end);  
}  

int getProduct(int A[], int j, int n)
{
	int tmp = A[j];
	for (int i = j; i < n-1; i++)
	{
		if (A[i] <= A[i + 1])
			tmp *= A[i+1];
		else
			return tmp;
	}

	return tmp;
}

int maxProduct(int A[], int n) 
{
	vector<int> Product;
	int tmp = 0;
	for (int i = 0; i < n; i++)
	{
		Product.push_back(getProduct(A, i, n));
	}

	tmp = Product[0];
	for (vector<int>::iterator i = Product.begin()+1; i != Product.end(); i++)
	{
		tmp = (tmp>*i) ? (tmp) : (*i);
	}

	return tmp;
}

