#include <iostream>
#include <vector>
#include <stdio.h>
#include <limits.h>
#include <float.h>


using namespace std;

void Qsort(int a[], int low, int high)
{
	if (low >= high)
	{
		return;
	}
	int first = low;
	int last = high;
	int key = a[first];/*用字表的第一个记录作为枢轴*/
	while (first<last)
	{
		while (first<last&&a[last] >= key)
			--last;
		a[first] = a[last];/*将比第一个小的移到低端*/
		while (first<last&&a[first] <= key)
			++first;
		a[last] = a[first];/*将比第一个大的移到高端*/
	}
	a[first] = key;/*枢轴记录到位*/
	Qsort(a, low, first - 1);
	Qsort(a, first + 1, high);
}

void main()
{
	
	int A[] = { 1, 2, 3, 4, 6, 7, 14, 15, 20, 35, 45, 55, 56, 57, 58 };
	int B[] = { 9, 5, 38 };
	int m = sizeof(A) / sizeof(A[0]);
	int n = sizeof(B) / sizeof(B[0]);
	int i = 0;
	int j = 0;
	vector<int> tmp;

	Qsort(B, 0, n - 1);
	i = 0;
	j = 0;
	while (i < m && j < n)
	{
		if (A[i] > B[j])
		{
			tmp.push_back(B[j]);
			j++;
		}
		else
		{
			tmp.push_back(A[i]);
			i++;
		}
	}

	while (i < m)
	{
		tmp.push_back(A[i]);
		i++;
	}
	while (j < n)
	{
		tmp.push_back(B[j]);
		j++;
	}
	
	for (i = 0; i < m+n; i++)
	{
		cout << tmp.at(i) << ' ';
	}


	system("PAUSE");
}


